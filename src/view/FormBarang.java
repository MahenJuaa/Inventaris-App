    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    String nama = txtNama.getText();
    String stokText = txtStok.getText();

    // Validasi
    if (nama.isEmpty() || stokText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
        return;
    }

    int stok;
    try {
        stok = Integer.parseInt(stokText);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Stok harus angka!");
        return;
    }

    if (controller.insert(nama, stok)) {
        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");

        loadTable(); 

        txtNama.setText("");
        txtStok.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menambahkan data!");
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

