    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dulu!");
        return;
    }

    // KONFIRMASI HAPUS
    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Yakin ingin menghapus data?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    // EKSEKUSI DELETE
    if (controller.delete(selectedId)) {
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

        loadTable();
        selectedId = -1;

        // RESET FORM
        txtNama.setText("");
        txtStok.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dulu!");
        return;
    }

    String nama = txtNama.getText();
    String stokText = txtStok.getText();

    // VALIDASI KOSONG
    if (nama.trim().isEmpty() || stokText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Data tidak boleh kosong!");
        return;
    }

    // VALIDASI ANGKA
    int stok;
    try {
        stok = Integer.parseInt(stokText);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Stok harus angka!");
        return;
    }

    // VALIDASI NILAI
    if (stok < 0) {
        JOptionPane.showMessageDialog(this, "Stok tidak boleh negatif!");
        return;
    }

    if (controller.update(selectedId, nama, stok)) {
        JOptionPane.showMessageDialog(this, "Data berhasil diupdate");
        loadTable();
        selectedId = -1;

        // RESET FORM
        txtNama.setText("");
        txtStok.setText("");
    } else {
        JOptionPane.showMessageDialog(this, "Gagal update data!");
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

